<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Feature: Disable Previous Dates
        const dateInput = document.getElementById('moveDate');
        const today = new Date();
        const yyyy = today.getFullYear();
        let mm = today.getMonth() + 1;
        let dd = today.getDate();

        if (dd < 10) dd = '0' + dd;
        if (mm < 10) mm = '0' + mm;

        const minDate = yyyy + '-' + mm + '-' + dd;
        dateInput.setAttribute('min', minDate);
    });

    function toggleForm(type) {
        const bookingFields = document.querySelector('.booking-fields');
        const submitBtn = document.getElementById('submitBtn');
        const dynamicTitle = document.getElementById('dynamicTitle');
        const dynamicSub = document.getElementById('dynamicSub');
        const msgLabel = document.getElementById('msgLabel');

        if (type === 'booking') {
            bookingFields.style.display = 'flex';
            dynamicTitle.innerText = "Book Your Move";
            dynamicSub.innerText = "Enter your relocation details for a confirmed slot.";
            submitBtn.innerHTML = 'CONFIRM BOOKING <i class="bi bi-calendar-check-fill ms-2"></i>';
            msgLabel.innerText = "Specific packing instructions (e.g. fragile items)";
            
            // Set required fields for Booking
            document.getElementById('moveFrom').required = true;
            document.getElementById('moveTo').required = true;
            document.getElementById('moveDate').required = true;
            document.getElementById('moveSize').required = true;
        } else {
            bookingFields.style.display = 'none';
            dynamicTitle.innerText = "Send a Message";
            dynamicSub.innerText = "We usually respond within 30 minutes.";
            submitBtn.innerHTML = 'SEND INQUIRY <i class="bi bi-send-fill ms-2"></i>';
            msgLabel.innerText = "Additional Requirements or Questions";

            // Remove required when hidden
            document.getElementById('moveFrom').required = false;
            document.getElementById('moveTo').required = false;
            document.getElementById('moveDate').required = false;
            document.getElementById('moveSize').required = false;
        }
    }
</script>

